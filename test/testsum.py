def clean_text(text_content):
    """Removes source tags and extra whitespace."""
    text_content = re.sub(r'\'', '', text_content)
    text_content = re.sub(r'ï¼​', '', text_content) # Remove the specific unicode character
    return text_content.strip()

def parse_job_entry(entry_text):
    job_position = "N/A"
    date_range = "N/A"
    company = "N/A"
    city = "N/A"
    state = "N/A"

    lines = [line.strip() for line in entry_text.split('\n') if line.strip()]

    if not lines:
        return {
            "job_position": job_position,
            "date_range": date_range,
            "company": company,
            "company_city": city,
            "company_state": state
        }

    # Job Position is usually the first meaningful line or after section header
    # Check if 'Work Experience' is the very first line, then job position is next
    if lines[0].lower().startswith("work experience") or lines[0].lower().startswith("professional experience"):
        if len(lines) > 1:
            job_position = lines[1]
            lines = lines[2:] # Remove header and job position
        else:
            lines = [] # No more lines to parse
    else:
        job_position = lines[0]
        lines = lines[1:]

    # Remove irrelevant header phrases from job position
    job_position = re.sub(r"^(Experience|Summary|Highlights|Career Overview|Skills)\s*", "", job_position, flags=re.IGNORECASE).strip()


    # Try to find date range
    date_match = re.search(r'(?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec|\d{2}\/\d{4})\s*(?:to|-)?\s*(?:(?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)\s+\d{4}|\d{2}\/\d{4}|Current|\d{4})', " ".join(lines), re.IGNORECASE)
    if date_match:
        date_range = date_match.group(0).strip()
        # Remove date range from lines to avoid re-matching
        entry_text_without_dates = " ".join(lines).replace(date_range, '', 1) # Replace only once
        lines = [line.strip() for line in entry_text_without_dates.split('\n') if line.strip()]

    # Find company name and location
    company_location_match = re.search(r'(?P<company>(?:Company Name|Siemens Building Technologies Pvt\. Ltd\.?|Schneider Electric India Pvt\.? Ltd)\s*[^,\n]+?)(?:,\s*|\s*[-–]\s*)(?P<city>[A-Za-z\s\.]+)\s*,\s*(?P<state>[A-Za-z\s\.]+)', " ".join(lines), re.IGNORECASE)

    if company_location_match:
        company = company_location_match.group('company').strip()
        city = company_location_match.group('city').strip()
        state = company_location_match.group('state').strip()
        # Clean specific company names if generic "Company Name" is used
        if "Company Name" in company:
            company = "Company Name" # Standardize to this placeholder if matched
        elif "Siemens" in company:
            company = "Siemens Building Technologies Pvt. Ltd."
        elif "Schneider" in company:
            company = "Schneider Electric India Pvt. Ltd"

    # Fallback for job position if not well-captured initially
    if job_position == "N/A" and lines:
        job_position = lines[0]


    # Clean job position one more time
    job_position = job_position.replace("to Current", "").strip()


    return {
        "job_position": job_position,
        "date_range": date_range,
        "company": company,
        "company_city": city,
        "company_state": state
    }

def extract_job_history_final(text_content):
    cleaned_text = clean_text(text_content)
    job_entries = []

    # Regex to split the text into potential job blocks.
    # It looks for a job title line, followed by a date line or company line.
    # We use a non-greedy match to capture the content of each block.
    # This pattern tries to identify the start of a new job entry.
    job_section_start_keywords = r"(?:Work Experience|Experience|Professional Experience)"
    job_entry_start_pattern = re.compile(
        r"(?P<job_title>(?:^|\n)\s*[A-Z][a-zA-Z\s\/,\(\)\&\-\.]+?)\s*\n+" # Job Title line
        r"(?P<rest_of_entry>[\s\S]*?)?" # Rest of the entry
        r"(?=\n\s*(?:(?:[A-Z][a-zA-Z\s\/,\(\)\&\-\.]+\s*(?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec|\d{2}\/\d{4}))|%s|\Z))" % job_section_start_keywords, # Lookahead for next job title, section start, or end of string
        re.VERBOSE | re.IGNORECASE | re.MULTILINE
    )

    # First, find the "Work Experience" or "Experience" section
    experience_section_match = re.search(r"(%s)[\s\S]*?(?=Educational Background|Education|Skills|Professional Affiliations|\Z)" % job_section_start_keywords, cleaned_text, re.VERBOSE | re.IGNORECASE)

    if experience_section_match:
        experience_text = experience_section_match.group(0)
        # Remove the section header from the text to be parsed as entries
        experience_text = re.sub(r"^(?:%s)\s*\n" % job_section_start_keywords, "", experience_text, flags=re.IGNORECASE | re.MULTILINE)

        # Split into individual job blocks more reliably
        # A job block starts with a title and is followed by dates or company name
        # It ends before another job title/date or end of section
        individual_job_block_pattern = re.compile(r"""
            (
                (?:^|\n)\s* # Start of line or after a newline
                (?P<job_position>[A-Z][a-zA-Z\s\/,\(\)\&\-\.]+?) # Job Title
                (?:\s*\n+                       # Followed by one or more newlines
                (?:(?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec|\d{2}\/\d{4})[^ \n]* # Date format
                |Company Name                   # Or Company Name
                |[-\s]*Company Name             # Or - Company Name
                )[\s\S]*?                     # Match all content non-greedily
            )
            (?=
                (?:^\s*[A-Z][a-zA-Z\s\/,\(\)\&\-\.]+?\s*\n+\s*(?:(?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec|\d{2}\/\d{4})|Company Name|-\s*\d{2}\/\d{4}|,\s*\d{2}\/\d{4})) # Lookahead for next job title with date/company
                |
                ^\s*(?:Skills|Professional Overview|Career Overview|Summary|Highlights|Accomplishments|Education|\Z) # Lookahead for next main section or end of string
            )
        """, re.VERBOSE | re.MULTILINE)

        for match in individual_job_block_pattern.finditer(experience_text):
            block_content = match.group(0).strip()
            parsed_data = parse_job_entry(block_content)
            if parsed_data["job_position"] != "N/A": # Only add if a job position was actually found
                job_entries.append(parsed_data)
    return job_entries

def parse_education_entry(entry_text):
    degree_type = "N/A"
    degree_field = "N/A"
    year = "N/A"
    institution = "N/A"
    city = "N/A"
    state = "N/A"

    lines = [line.strip() for line in entry_text.split('\n') if line.strip()]

    # Pattern to find degree, field, and year on first or second lines
    degree_line_pattern = re.compile(r"(?P<degree_type>(?:Master(?:'s)?|Bachelors? of Arts|Associate of Applied Science|Bachelor of Instrumentation Engineering|Master of Arts|Associates?))(?:\s*,\s*:\s*|\s*:\s*|\s*,)?\s*(?P<degree_field>[A-Za-z\s,\&\.]+?)?\s*(?:,\s*)?(?P<year>\d{4}(?:-\d{2}-\d{2})?)", re.IGNORECASE)
    
    # Try to extract degree, field, and year from the first few lines
    for i in range(min(3, len(lines))): # Check first 3 lines
        match = degree_line_pattern.search(lines[i])
        if match:
            degree_type = match.group('degree_type').strip()
            degree_field = match.group('degree_field').strip() if match.group('degree_field') else 'N/A'
            year = match.group('year').strip()
            break
    
    # Institution and location are usually after degree/year
    # Look for the line containing "University" or "College"
    institution_location_pattern = re.compile(r"(?P<institution>(?:[A-Za-z\s\.]+?)\s*(?:University|College|Stratford|Forsyth|Community College of the Air Force))[\s\S]*?(?P<city>[A-Za-z\s]+?)\s*,\s*(?P<state>[A-Za-z\s]+)(?:,\s*USA)?", re.IGNORECASE)

    institution_match = institution_location_pattern.search(entry_text)
    if institution_match:
        institution = institution_match.group('institution').strip()
        city = institution_match.group('city').strip()
        state = institution_match.group('state').strip()
        
    # Special handling for "Stratford" as institution without "University"
    if "Stratford" in entry_text and institution == "N/A":
        stratford_match = re.search(r"Stratford\s*\n+\s*([A-Za-z\s]+?)\s*,\s*([A-Za-z\s]+)(?:,\s*USA)?", entry_text)
        if stratford_match:
            institution = "Stratford"
            city = stratford_match.group(1).strip()
            state = stratford_match.group(2).strip()

    return {
        "degree_type": degree_type,
        "degree_field": degree_field,
        "year": year,
        "institution": institution,
        "institution_city": city,
        "institution_state": state
    }

def extract_education_final(text_content):
    cleaned_text = clean_text(text_content)
    education_entries = []

    # Find the "Education" or "Educational Background" section
    education_section_match = re.search(r"(Educational Background|Education and Training|Education)[\s\S]*?(?=Certifications and Trainings|Skills|Professional Affiliations|Interests|\Z)", cleaned_text, re.VERBOSE | re.IGNORECASE)

    if education_section_match:
        education_text = education_section_match.group(0)
        # Remove the section header from the text to be parsed as entries
        education_text = re.sub(r"^(?:Educational Background|Education and Training|Education)\s*\n", "", education_text, flags=re.IGNORECASE | re.MULTILINE)

        # Split into individual education blocks. Each block typically starts with a degree type.
        individual_education_block_pattern = re.compile(r"""
            (
                (?:^|\n)\s* # Start of line or after a newline
                (?:Master(?:'s)?|Bachelors? of Arts|Associate of Applied Science|Bachelor of Instrumentation Engineering|Master of Arts|Associates?) # Degree type
                [\s\S]*?                     # Match all content non-greedily
            )
            (?=
                (?:^\s*(?:Master(?:'s)?|Bachelors? of Arts|Associate of Applied Science|Bachelor of Instrumentation Engineering|Master of Arts|Associates?)) # Lookahead for next degree
                |
                \Z # End of string
            )
        """, re.VERBOSE | re.MULTILINE)

        for match in individual_education_block_pattern.finditer(education_text):
            block_content = match.group(0).strip()
            parsed_data = parse_education_entry(block_content)
            if parsed_data["degree_type"] != "N/A": # Only add if a degree type was found
                education_entries.append(parsed_data)
    return education_entries


